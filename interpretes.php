    }                                         

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:
        if (rbSencillo.isSelected()){
            total = total + 30;
        }
        else {
            if (rbDoble.isSelected()){
                total = total + 40;
            }
            else {
                JOptionPane.showMessageDialog(null, "Seleccione una opción correcta");
            }
        }
        txtTotal.setText(Double.toString(total));
    }                                          

    private void rbDobleActionPerformed(java.awt.event.ActionEvent evt) {                                        
        // TODO add your handling code here:
        if (rbDoble.isSelected()){
            lblHelado2.setVisible(true);
        cmbSabor2.setVisible(true);
        }
    }                                       

    private void rbSencilloActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:
