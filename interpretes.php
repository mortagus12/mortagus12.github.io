    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        try{
        recibo = Double.parseDouble(txtCambio.getText());
        btnAgregar.setEnabled(false);
        rbSencillo.setEnabled(false);
        rbDoble.setEnabled(false);
        if (recibo < total){
            total = total - recibo;
            lblCambio.setText("Dinero faltante");
            txtCambio1.setText(Double.toString(total));
        }
        else{
            if (recibo == total){
                total = 0;
                lblCambio.setText("Cambio");
                txtCambio1.setText("Pago exacto, no hay cambio");
                btnPagar.setEnabled(false);
            }
            else {
                cambio = recibo - total;
                lblCambio.setText("Cambio");
                txtCambio1.setText(Double.toString(cambio));
                btnPagar.setEnabled(false);
            }
        }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Hubo un error, verifica haber insertado un valor correcto e intenta de nuevo");
        }
    }                                        

    private void btnLimpiarActionPerformed(java.awt.event.ActionEvent evt) {                                           
        // TODO add your handling code here:
        total = 0;
        lblHelado2.setVisible(false);
        cmbSabor2.setVisible(false);
        btnPagar.setEnabled(true);
        btnAgregar.setEnabled(true);
        txtTotal.setText("");
        txtCambio.setText("Insertar dinero");
        lblCambio.setText("Cambio");
        txtCambio1.setText("");
        rbSencillo.setEnabled(true);
        rbDoble.setEnabled(true);
        btnGpHelado.clearSelection();
    }                                          

